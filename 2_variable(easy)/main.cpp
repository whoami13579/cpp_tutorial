#include <iostream>
#include <string>

int main() {
    // int
    int i = 10;
    std::cout << "i = " << i << std::endl;
    int i2 = -10;
    std::cout << "i2 = " << i2 << std::endl;
    
    std::cout << "----------" << std::endl;

    // float
    float f = 5;
    std::cout << "f = " << f << std::endl;

    float f2 = 1.5;
    std::cout << "f2 = " << f2 << std::endl;

    std::cout << "----------" << std::endl;

    // char
    char c = 'A';
    std::cout << "----------" << std::endl;

    // string
    std::string str = "hello";
    std::cout << "str = " << str << std::endl;

    std::cout << "----------" << std::endl;

    // bool
    bool b = true;
    std::cout << "b = " << b << std::endl;

    bool b2 = false;
    std::cout << "b2 = " << b2 << std::endl;

    std::cout << "----------" << std::endl;
    return 0;
}